    <h2>PSU Vanguard</h2>
    <p>Starting the winter term of 2018, I became the creative directer of the Portland State Vanguard, a weekly student run newspaper. My main roles at the Vanguard were to manage a team of designers, create weekly covers/center spreads to accompany news stories, design special guides, art direct photo shoots, and curate a weekly comics page. Working for the VG has been one of the most rewarding opportunities I’ve had the honor of accepting. The VG has taught me how to manage time, people, stress, and most of all, how to grab a readers attention. I value my time spent in this role and cherish the friendships and work I’ve made through the Vanguard.</p>

    <img class="artwork" src="students/winter18//a_ughoc_image2.jpg" alt="Photo of Project 2">
    <h2>Two Things</h2>
    <p>Two things is a personal project I started last summer as a creative exercise when I had nothing going on. The idea of the project is to take two completely unrelated images that are buried deep in royalty free archives and combine them in digital photo collage in order to create something new and interesting. Most great design combines two things to solve a visual problem and this project has become a warm up exercise to get those creative juices flowing.</p>

