    <h2>A&O Connect</h2>
    <p>Last year two students started the Athletic and Outdoor Connection at Portland State University with the hopes of connecting students to the A&O industry and bridging connections to industry leaders. Once a month they invite industry professionals to speak to students about their respective careers. This rebrand aims to visually content the outdoors with student life and the forward thinking spirit of the group in an effort to increase membership and brand awareness.</p>

