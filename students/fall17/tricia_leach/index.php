<h2>Be Honest Student Showcase</h2>
<p>Web development and design, art direction, project management, and social media for PSU's annual graphic design portfolio show at Wieden+Kennedy. Having the opportunity to lead a team of student volunteers, and collaborate with 140+ students to put on PSUGD's biggest event of the year made this a dream project.</p>
<img class="artwork" src="students/fall17/tricia_leach/tleach_image2.gif" alt="Mars Interactive Poster">
<h2>Mars Travel Poster</h2>
<p>A interactive reinterpretation of a print poster from NASA's Visions of the Future travel series. This was an exercise in learning CSS transforms, animations, and jQuery. This project sparked an interest and future experimentations in motion and interactivity for the web.</p>
<img class="artwork" src="students/fall17/tricia_leach/tleach_image3.jpg" alt="FoGD Website">
<h2>Time Travel App</h2>
<p>This speculative app delivers the information and resources a time traveler would need to traverse safely across space and time. Features include recommended locations and events, travel alerts for periods of war and unrest, and a saved travel itinerary. Research for the project included user stories, wireframes, and a map of user flows prior to moving to the design stage.</p>
<img class="artwork" src="students/fall17/tricia_leach/tleach_image4.jpg" alt="Arduino Lightchimes">
<h2>Arduino Lightchimes</h2>
<p>For design thesis, Tricia taught herself how to use Arduino circuit boards and sensors to code and build a light installation at a public park. The lightchimes were triggered both by wind and human movement, and cycled through rainbow colors. This period of self-study was the catalyst for future projects using various creative programming tools and languages.</p>
