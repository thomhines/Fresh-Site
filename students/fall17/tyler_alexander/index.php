    <h2>The In-Joke</h2>
    <p>Memes are visual jokes we share online, but they’re more than just jokes, they’re cultural artifacts of the subcultures in which they are created and a dominant method of establishing narratives.</p><p>After the 2016 election, I realized that memes have immense power in our socio-political landscape. I felt as though there was a need for a primer on the topic of memes, so I created the In-Joke which is a 175 page book of my research, writing, and information design on the history of memes, their role in politics, and speculations on the future of memes.</p>
    <img class="artwork" src="students/fall17/tyler_alexander/talexander_image2.jpg" alt="Intersect">
    <h2>Intersect</h2>
    <p>Politics aren’t an optional attribution to design, I believe they are present in everything we do as designers.</p><p>
There was a marked lack of critical conversations happening around design and politics among the students at Portland State, so I designed the identity for a movie night and discussion group called Intersect to spark those conversations. I brought the event to life with the first screening and discussion occurring in spring 2017.</p>
    <img class="artwork" src="students/fall17/tyler_alexander/talexander_image3.jpg" alt="Rojava Travel Guide">
    <h2>Rojava Travel Guide</h2>
    <p>In 2016, I found out that the singer of one of my favorite punk bands had left his job as a florist in San Francisco and writer at Willamette Week to go fight ISIS alongside a feminist, leftist Kurdish militia called the People’s Protection Units, or YPG, in the Syrian civil war.</p><p>
This news blew me away and raised so many questions. To answer these questions for myself and others, I decided to create a travel guide to Northern Syria, more commonly known as Rojava.</p>
    <img class="artwork" src="students/fall17/tyler_alexander/talexander_image4.gif" alt="Mira Shah Art Therapy">
    <h2>Mira Shah Art Therapy</h2>
    <p>Art therapy as a practice is primarily concerned with the cathartic act of mark-making, the accumulation of process, and connecting the pieces.</p><p>
The identity I created for Mira Shah embodies those attributes through a grid system which enables Mira to give all forms of official communication a personalized touch by using the grid to sign her initials on all forms of communication. This flexible branding system finds the sweet spot between clinical and creative, which is where art therapy resides.</p>
