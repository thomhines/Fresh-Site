    <h2>Modo Boxed Wine</h2>
    <p>This project involved creating a brand and packaging system for a beverage of our choice. Modo Wine Co. prides themselves on “thinking outside the box,” seen through a set of three dynamic logos that change with each new wine. The bright colors that signify the tasting notes, the dynamic logo and pattern, and the playful language all help to create a tone that would catch the attention of their targeted millennials.</p>
    <img class="artwork" src="students/fall17/tina_roach/troach_image2.jpg" alt="MB&I Branding">
    <h2>MB&I Branding</h2>
    <p>The band, My Brothers and I, needed to further their brand identity by creating more assets like a record cover, a single cover and various merchandise. I wanted to hint at their idealistic and yet grounded central themes, while visually providing plenty of abstract cues for future records. Trying not to sway too much in any genre direction, I managed to settle on a composition for the record above that feels dynamic and unique, while still furthering the story they currently were telling, “Don’t Dream Alone.”</p>
    <img class="artwork" src="students/fall17/tina_roach/troach_image3.gif" alt="Great Clips Rebrand">
    <h2>Great Clips Rebrand</h2>
    <p>When I think of Great Clips, I think of a salon that my dad might go to, not a place for young people. In learning about the company’s pillar of efficiency, I wanted to discover a look and feel that speaks to younger people while still keeping their current customers. The rebrand is fresh, simple, and targets a younger audience with a playful, “no frills” mentality, while remaining transparent enough to keep their existing consumers.</p>
    <img class="artwork" src="students/fall17/tina_roach/troach_image4.jpg" alt="Outside In Collection">
    <h2>Outside In Collection</h2>
    <p>The Outside In collection is for the artist who has a hard time sitting still at a desk. The patterns aim to bring a sense of the outdoors inside the four walls of an office or cubicle. Products include wallpaper, lamp shades, vases, framed prints, and chairs. The variety of options lend itself well to an audience of people who like to be creative and mix it up!</p>
