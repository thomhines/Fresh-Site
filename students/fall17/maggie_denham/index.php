    <h2>Minimize</h2>
    <p>Minimize is a newspaper that shows how an average person can incorporate minimalism into their daily life. Inside I discuss beauty products, my living space, my closet, and my own little museum of useless objects. The design reflects some of the mess created in the cleaning process and an exploration of the minimalist design aesthetic.</p>
