    <h2>Independent Publishing Resouce Center</h2>
    <p>The Independent Publishing Resouce Center (IPRC) asked for help with their event promotion, kickstarter campaign, community outreach, and upcoming membership drive. As a graphic design intern I create monthly calendars, social media posts, and other promotional material using the valuable resources the IPRC provides to the DIY community in Portland.</p>
    <img class="artwork" src="students/fall17/maggie_denham/mdenham_image3.jpg" alt="Forage Home">
    <h2>Forage Home</h2>
    <p>Forage Home is an online decor shop based in Sacramento, California that asked for a brand identity that reflects their clean, effortless style. This included a versatile and professional logo design that appeals to both their high-end clientele as well as the young professional generation moving to Sacramento from the Bay area.</p>
    <img class="artwork" src="students/fall17/maggie_denham/mdenham_image4.jpg" alt="Mira Shah">
    <h2>Mira Shah</h2>
    <p>Mira Shah is an Art Therapist who needed a brand to reflect her fun and professional image. I teamed up with student designer Paige Landis to create a brand that utilizes the playful art mediums used in her practice. The primary logo represents the inner eye of wisdom and awakening, drawing from Mira Shah’s education in meditation and mindfulness.</p>
