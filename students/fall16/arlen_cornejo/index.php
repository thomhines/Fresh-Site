        <p>Having lived between El Salvador and Portland, OR I acquired a sensitivity to contrast in culture, color and patterns found in everyday life. I am always trying to find connections between concepts, images, and texture. I enjoy creating designs with a hand-done quality while being mindful of practicality and function without losing sight of the details. In my free time, I enjoy writing letters, watching movies, cooking Latin food and riding my bike around Portland.</p>
