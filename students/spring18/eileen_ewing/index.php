    <h2>Be Honest 2018</h2>
    <p>Be Honest is the annual PSUGD student showcase held at Wieden + Kennedy where students display their work and have an opportuinty to talk about and get feedback from design professionals. With a team of other PSU students I worked on creating a campaign around the theme of 'Falling into Place'. Playing off the idea of Rube Goldburg machines where there is a serise of small chain reactions that achieve an ultimate goal, we related this to the student experience of finding place in the design world.</p>

