    <h2>Torch</h2>
    <p>Torch is a newspaper that focuses on female ceramicists, highlighting the rad women working in this art from. One of the goals for this project was to step outside of my comfort zone and work with more modern type. Throughout the publication I used type and image treatment to mimic the smooth, hand formed nature of ceramics. I also referenced the process of glazing by using overlays and layers.</p>

