    <h2>The Intersection Between Cowboys and Homosexuality</h2>
    <p>The Intersection Between Cowboys and Homosexuality is a look into the intersecting topics of cowboys and homosexuality. I wanted to explore the idea of “homos on the range” through re-examining history of the early Western Civilization through a queer perspective.</p>

