    <h2>A Season in Hell Reissue</h2>
    <p>This project stemmed from the idea of wanting to alter the viewer’s perception and prior knowledge on what a book is. This required me to think about the opposition in bookmaking—thinking about the deconstruction of a book or rather components of a book that can exist outside of normal book conventions.</p>

