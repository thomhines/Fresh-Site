    <h2>Drink Yoncé</h2>
    <p>Drink Yoncé is a website I fully designed and coded. The site showcases four cocktails inspired by Beyonce's branding and music. I created collages in her signature style and added an appear on hover affect to mimic the stop motions style of her Instagram videos.</p>

